    public int mesaExisteEnDB(String valor){
        try {
            conection conectar = new conection();
            CallableStatement cts = null;
            Connection con = conectar.getConection();
                ResultSet rs;
                String query = "{CALL VALIDAR_MESA(?)}";
                    cts = con.prepareCall(query);
                        cts.setString(1, valor);
                            rs = cts.executeQuery();
                                if(rs.next()){
                                    //si hay un registro significa que no podemos usarlo, por lo tanto
                                    return rs.getInt(1);
                                }
                                    return 1;
                        
        } catch (ClassNotFoundException | SQLException e) {
            return 1;
        }
    }
    
